    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }
    
    func getContext () -> NSManagedObjectContext {
        let appDelegate = UIApplication.shared.delegate as! AppDelegate
        return appDelegate.persistentContainer.viewContext
    }
    
    override func viewDidAppear(_ animated: Bool) {
        super.viewDidAppear(animated)
        
        let context = self.getContext()
        let fetchRequest = NSFetchRequest<NSManagedObject>(entityName: "PresentList")
        
        let sortDescriptor = NSSortDescriptor (key: "presentItem", ascending: true)
        fetchRequest.sortDescriptors = [sortDescriptor]
        
        do {
            presentArray = try context.fetch(fetchRequest)
        } catch let error as NSError {
            print("Could not fetch. \(error), \(error.userInfo)")
        }
        
        self.tableView.reloadData()
        
    }
    

    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return presentArray.count
    }

    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "Present Cell", for: indexPath)

        // Configure the cell...
        let list = presentArray[indexPath.row]
        var display: String = ""
        
        if let presentLabel = list.value(forKey: "presentItem") as? String {
            display = presentLabel
        }
        
        cell.textLabel?.text = display
        cell.detailTextLabel?.text = list.value(forKey: "target") as? String
        
        return cell
    }
    

